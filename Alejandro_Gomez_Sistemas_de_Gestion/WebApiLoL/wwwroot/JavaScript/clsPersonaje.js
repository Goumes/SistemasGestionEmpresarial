class Personaje {

    constructor(idPersonaje, nombre, alias, vida, regeneracion, danno, armadura, velAtaque, resistencia, velMovimiento) {
        this.idPersonaje = idPersonaje;
        this.nombre = nombre;
        this.alias = alias;
        this.regeneracion = regeneracion;
        this.vida = vida;
        this.danno = danno;
        this.armadura = armadura;
        this.velAtaque = velAtaque;
        this.resistencia = resistencia;
        this.velMovimiento = velMovimiento;
    }
}